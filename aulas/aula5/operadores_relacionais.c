#include <stdio.h>

int main() {

    // < menor que
    // > maior que
    // <= menor igual
    // >= maior igual
    // == igual
    // != diferente

    printf("2 eh maior igual a 5? %i \n", 2 >= 5);
    printf("2 eh maior que 5? %i \n", 2 > 5 );
    printf("2 eh menor igual a 5? %i \n", 2 <= 5);
    printf("2 eh menor que 5? %i \n", 2 < 5);
    printf("2 eh igual a 5? %i \n", 2 == 5);
    printf("2 eh diferente de 5? %i \n", 2 != 5);

    return 0;
}